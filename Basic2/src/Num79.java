import java.util.Scanner;

public class Num79 {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		
		
		
		while(true)
		{
			char x = sc.next().charAt(0);
			if(x == 'q')
			{
				System.out.println(x);
				break;
			}
			else
			{
				System.out.println(x);
			}
		}
		

	}

}
