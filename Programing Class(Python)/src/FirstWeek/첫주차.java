package FirstWeek;

public class 첫주차 {

	public static void main(String[] args) {
		
		long A = 7995868;
		long B = 4474694;

		System.out.println("A+B를 구하시오 : " + A + B);
		System.out.println("A*B를 구하시오 : " + (A * B));
		System.out.println("A-B를 구하시오 : " + (A - B));
		System.out.println("A/B를 구하시오 : " + (A / B));
		System.out.println("A%B를 구하시오 : " + (A % B));
		System.out.println("A^5를 구하시오 : " + Math.pow(A, 5));
		

	}

}
