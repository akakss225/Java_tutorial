
public class 일단끄적거리기 {

	public static void main(String[] args) {

		int sum = 0;
		for(int i = 0; i <= 10; i++)
			sum += i;
		System.out.println("(1~10)합 : " + sum);
		
		sum = 0;
		for(int j = 10; j <= 100; j++)
			sum += j;
		System.out.println("(10~100)합 : " + sum);
		
		sum = 0;
		for(int k = 100; k <= 1000; k++)
			sum += k;
		System.out.println("(100~1000)합 : " + sum);
		
		}

	}


