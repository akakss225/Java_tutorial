package 기초;

public class 반복문 {

	public static void main(String[] args) {
		int i = 1;
		
		while(i <= 5) {
			System.out.println("Coding Everybody!");
			
			i++;
		}
		
		for(int j = 1 ; j <= 5; j++)
		{
			if(j <= 5)
			{
				System.out.println("The for can replace the while like this!!");
			}
		}
		
		
		

	}

}
